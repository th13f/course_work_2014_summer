package by.bsu.coursework.structures;

/**
 *
 * @author th13f
 */
public class Edge {
    private int from;
    private int to;

    public Edge(int from, int to) {
        this.from = from;
        this.to = to;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public int getTo() {
        return to;
    }

    public void setTo(int to) {
        this.to = to;
    }
    
    @Override
    public Edge clone(){
        return new Edge(from,to);
    }
}
